// Define Employee model with picture URL
class Employee {
  final String name;
  final String jobTitle;
  final String department;
  final String pictureUrl;
  final String email;
  final String phone;

  Employee({
    required this.name,
    required this.jobTitle,
    required this.department,
    required this.pictureUrl,
    required this.email,
    required this.phone,
  });
}

